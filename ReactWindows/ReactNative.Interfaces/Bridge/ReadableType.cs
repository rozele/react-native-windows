namespace ReactNative.Core.Bridge
{
    /// <summary>
    /// Types of data model values supported by the bridge.
    /// </summary>
    public enum ReadableType
    {
        /// <summary>
        /// Null type.
        /// </summary>
        Null,

        /// <summary>
        /// Boolean type.
        /// </summary>
        Boolean,

        /// <summary>
        /// Number type.
        /// </summary>
        Number,

        /// <summary>
        /// String type.
        /// </summary>
        String,

        /// <summary>
        /// Map type.
        /// </summary>
        Map,

        /// <summary>
        /// Array type.
        /// </summary>
        Array,
    }
}
